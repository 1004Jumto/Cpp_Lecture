#include<iostream>

using namespace std;

int main(void) {
	int num;
	string cat;

	cin >> num;
	cout << "Please Enter a Integer: ";

	if (num % 2 == 0) {
		cat = "EVEN";
	}
	else{
		cat = "ODD";	
	}

	cout << "The Value " << num << " is an " << cat << " number.";
	
	return 0;
}
